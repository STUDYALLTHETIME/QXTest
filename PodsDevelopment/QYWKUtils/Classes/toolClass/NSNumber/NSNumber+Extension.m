#import "NSNumber+Extension.h"

@implementation NSNumber (Extension)

- (NSNumber *)addOne{
    
    NSInteger temp = self.integerValue;
    temp++;
    NSNumber *numResult = [NSNumber numberWithInteger:temp];
    return numResult;
}

- (NSNumber *)subOne{
    NSInteger temp = self.integerValue;
    temp--;
    NSNumber *numResult = [NSNumber numberWithInteger:temp];
    return numResult;
}

@end
